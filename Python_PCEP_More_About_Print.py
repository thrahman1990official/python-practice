# print with single quotes around word
print('Hello!')
# output: Hello!
# if a single quote is part of a word or sentence then you will need to use double quotes
print("I'm studying Python")
# output: I'm studying Python
# or you can use single quotes in this format
print('I\'m studying Python')
print('and having fun!')
# output: 
# I'm studying Python
# and having fun!


