first_bit = 1
second_bit = 0
#In Boolean algebra below is first_bit LOGICAL AND second_bit
print(first_bit & second_bit)

first_bit = 0
second_bit = 0
#In Boolean algebra below is first_bit LOGICAL OR 0
print(first_bit | second_bit)

first_bit = 0
second_bit = 0
#In Boolean algebra below is 1 LOGICAL XOR 0 which results in 0
print(first_bit ^ second_bit)

print(~0)
